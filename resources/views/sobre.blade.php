    <h1 style="color:mediumseagreen">Sistema de Gestão de Frota de Camiões</h1>
    <p style="backgroundcolor:black">O Sistema de Gestão de Frota de Camiões é uma empresa sediada na cidade e província de Maputo, que tem como objetivo proporcionar ao estimado cliente a possibilidade de transporte a sua mercadoria para qualquer ponto do país, a qualquer momento, e em melhores condições de transporte e segurança. </p>
    <p>Proporciomos também ao cliente a possibilidade de alugar um oualguns dos nossos camiões para os seus serviços particulares.</p>
     <p>Oferecemos os nossos serviços ao melhor preço a nível nacional.</p>

    <h2 style="color:mediumseagreen">Visão</h2>
    <p>A SGFC, Lda tem como visão sustentada no seguinte:
            <li>Apresentar uma evolução consistente do seu volume de trabalho;</li>
            <li>Apresentar elevados padrões de desempenho ao nível de Qualidade, Segurança;</li>
            <li>Ambiente e Responsabilidade social;</li>
            <li>Reconhecimento no Mercado pela sua solidez, prestígio, inovação e progresso no
            profissionalismo, à audácia, à dedicação e a uma inigualável vontade de sempre
            querer mais e melhor;</li>
            <li>Crescer mas de forma sustentada e permanente;</li>
            <li>Reforçar a confiânça proporcionada aos nossos cliente em termos de cumprimento
            de todos requisitos contratuais;</li>
            <li>Optimizar todo o potencial dos seus colaboradores estimulando contínua melhoria
            das suas qualificações;</li>
            <li> Minimizar e eliminar os riscos de lesões e doenças profissionais;</li>
                    <li> Assegurar que os impactos ambientais nas actividades desenvolvidas sejam
            positivos;</li>
            <li>Adoptar modelos de gestão eficientes e rigorosos; </li>
            <li>Intervir em novs áreas de negócio e em novos mercados;</li>
            <li>Ser um exemplo no seu sector de actividade;</li>
        </p>
        </ul>
    <h2 style="color:mediumseagreen">Missão</h2>
    <p>A SGFC, Lda está vocacionada no procura de soluções e correspondência das mais altas
            exigências do mercado nacional. Tem como missão intervir com notoriedade, credibilidade
            na indústria transportadora e aluguer de trucks, sustentada por um conjunto de recursos
            humanos altamente qualificados e utilizando tecnologias em permanente actualização.
            Constitui uma preocupação constante preservar a mercadoria do cliente, promover a melhoria
            da qualidade de vida dos seus colaboradores e assegurar às necessárias condições de
            segurança de viagem.</p>
